    private void cmbrideActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbrideActionPerformed
        // TODO add your handling code here:
        
        String name = cmbride.getSelectedItem().toString();
        int quantity=0;
        double sum=0.0, p =0.0;
        try {
            Database db = new Database();
            db.connect();
            String sql = "select * from view_ride";
             db.pst = db.con.prepareStatement(sql);
            db.rs = db.pst.executeQuery();
            
            while(db.rs.next())
            {
                String a = db.rs.getString("ride_name");
                
                if(cmbride.getSelectedItem().equals(a))
                {
                   p = db.rs.getFloat("ride_price");
                   quantity+= db.rs.getInt("ride_quantity");
                   sum+= db.rs.getFloat("ride_totalsum");
                }
            }
            String price = String.valueOf(p);
            String q = String.valueOf(quantity);
            String s = String.valueOf(sum);
            tbxprice.setText(price);
            tbxticket.setText(q);
            tbxmoney.setText(s);
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_cmbrideActionPerformed

