    public void view_ride()
    {
        try {
            Database db = new Database();
            db.connect();
            String sql = "select ride_name from add_ride";
            db.pst = db.con.prepareStatement(sql);
            db.rs = db.pst.executeQuery();
            while(db.rs.next())
            {
                String name = db.rs.getString("ride_name");
                cmbride.addItem(name);
            }
        } catch (Exception e) {
            
            JOptionPane.showMessageDialog(null, e);
        }
        
                
    }
    
    
    
    
