    public void show_ride()
    {
        try {
            
            Database db = new Database();
            db.connect();
            String sql = "select ride_name from add_ride";
            db.pst = db.con.prepareStatement(sql);
            db.rs = db.pst.executeQuery();
            while(db.rs.next())
            {
                String a = db.rs.getString("ride_name");
                cmbride.addItem(a);
            }
            
        } catch (SQLException e) {
            
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
  
    
