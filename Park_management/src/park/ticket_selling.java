    private void cmbrideActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbrideActionPerformed
        // TODO add your handling code here:
        
        try {
            
            Database db = new Database();
            db.connect();
            String sql = "select * from add_ride";
            db.pst = db.con.prepareStatement(sql);
            db.rs = db.pst.executeQuery();
            while(db.rs.next())
            {
                String a = db.rs.getString("ride_name");
                String b = db.rs.getString("ride_price");
                if(cmbride.getSelectedItem().equals(a))
                {
                    tbxprice.setText(b);
                }
            }
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_cmbrideActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = cmbride.getSelectedItem().toString();
        double quantity, price, sum;
        quantity = Double.parseDouble(tbxquantity.getText());
        price = Double.parseDouble(tbxprice.getText());
        sum = quantity*price;
        String str = String.valueOf(sum);
        tbxsum.setText(str);
        
        try {
            Database db = new Database();
            db.connect();
            String sql ="insert into view_ride values (?,?,?,?)";
            db.pst = db.con.prepareStatement(sql);
            
            db.pst.setString(1, name);
            db.pst.setString(2, tbxprice.getText());
            db.pst.setString(3, tbxquantity.getText());
            db.pst.setString(4, tbxsum.getText());
            
            db.pst.execute();
        } catch (Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

